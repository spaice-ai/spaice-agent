def cmd_doctor(args: argparse.Namespace) -> int:
    agent_id = args.agent_id
    hook_dir = _hook_dir(agent_id)
    ok = True

    def check(label: str, passed: bool, detail: str = "") -> None:
        nonlocal ok
        mark = "✓" if passed else "✗"
        print(f"  {mark} {label}" + (f"  ({detail})" if detail else ""))
        if not passed:
            ok = False

    print(f"Doctor: spaice-{agent_id}")
    check("Hook dir exists", hook_dir.exists(), str(hook_dir))
    check("handler.py exists", (hook_dir / "handler.py").exists())
    check("HOOK.yaml exists", (hook_dir / "HOOK.yaml").exists())

    manifest_path = hook_dir / ".spaice-install.json"
    check("Install manifest exists", manifest_path.exists())

    if manifest_path.exists():
        try:
            manifest = json.loads(manifest_path.read_text())
            installed_ver = manifest.get("package_version", "?")
            current_ver = _get_version()
            check(
                "Shim version matches package",
                installed_ver == current_ver,
                f"shim={installed_ver} pkg={current_ver}",
            )
        except Exception as exc:
            check("Manifest parses", False, str(exc))

    try:
        import spaice_agent  # noqa: F401
        check("spaice_agent importable", True)
    except ImportError as exc:
        check("spaice_agent importable", False, str(exc))

    # BuildGuard v1 — governance middleware must be importable
    try:
        from spaice_agent.orchestrator import BuildGuard, BuildGuardDecision  # noqa: F401
        check("BuildGuard v1 active (pipeline enforcement)", True)
    except ImportError as exc:
        check("BuildGuard v1 active (pipeline enforcement)", False, str(exc))

    # Config file exists
    cfg_path = _config_path(agent_id)
    check("Config file exists", cfg_path.exists(), str(cfg_path))

    # Config loadable (deeper check — YAML parse + schema validate)
    if cfg_path.exists():
        try:
            from spaice_agent.config import load_agent_config
            load_agent_config(agent_id)
            check("Agent config loads + validates", True)
        except Exception as exc:
            check("Agent config loads + validates", False, str(exc))

    # Bundled skills — hard check (vetted, shipped with package)
    bundled_manifest = Path.home() / ".hermes" / "skills" / "spaice" / ".spaice-bundled-manifest.json"
    if bundled_manifest.exists():
        try:
            m = json.loads(bundled_manifest.read_text())
            skills_present = len(m.get("skills", []))
            check(
                "Bundled vetted skills installed",
                skills_present >= 3,
                f"{skills_present} skills — {', '.join(m.get('skills', []))}",
            )
        except Exception as exc:
            check("Bundled vetted skills readable", False, str(exc))
    else:
        check(
            "Bundled vetted skills installed",
            False,
            "run `spaice-agent skills bundled-install`",
        )

    # Antigravity skill library (standardised — vendored at pinned commit)
    antigravity_dir = Path.home() / ".hermes" / "skills" / "antigravity"
    if antigravity_dir.exists():
        skill_count = len(list(antigravity_dir.glob("*/SKILL.md")))
        check("Antigravity library installed", skill_count > 0,
              f"{skill_count} skills at {antigravity_dir}")
    else:
        check(
            "Antigravity library installed",
            False,
            "run `spaice-agent skills antigravity-install`",
        )

    # ------------------------------------------------------------------
    # Phase 3A — Memory vault + BuildGuard health
    # ------------------------------------------------------------------
    # Only meaningful once config is loadable (memory_root comes from there).
    if cfg_path.exists():
        try:
            from spaice_agent.config import load_agent_config
            _cfg = load_agent_config(agent_id)
        except Exception:
            _cfg = None

        if _cfg is not None:
            vault_root = Path(_cfg.memory_root).expanduser()
            check(
                "Memory vault root exists",
                vault_root.is_dir(),
                str(vault_root),
            )
            if vault_root.is_dir():
                check(
                    "Memory vault root writable",
                    os.access(vault_root, os.W_OK),
                    str(vault_root),
                )
                for sub in ("_inbox", "_continuity", "_dashboard"):
                    check(
                        f"Vault {sub}/ present",
                        (vault_root / sub).is_dir(),
                        str(vault_root / sub),
                    )

    # BuildGuard log dir (holds openrouter-*.jsonl) must be writable when present.
    # Created lazily on first pipeline call, so its absence is informational.
    build_log_dir = Path.home() / ".spaice-agents" / agent_id / "logs"
    if build_log_dir.is_dir():
        check(
            "BuildGuard log dir writable (openrouter-*.jsonl)",
            os.access(build_log_dir, os.W_OK),
            str(build_log_dir),
        )
    else:
        # Info only — no failure. BuildGuard mkdirs on first call.
        print(f"  · BuildGuard log dir not yet created  (will appear at {build_log_dir} on first pipeline call)")

    # BuildGuard exemption file MUST NOT linger (tactical escape-hatch only)
    exemption_path = Path.home() / ".spaice-agents" / agent_id / "build-exemption.yaml"
    check(
        "BuildGuard exemption absent (no lingering escape-hatch)",
        not exemption_path.exists(),
        str(exemption_path) if exemption_path.exists() else "",
    )

    return 0 if ok else 1


