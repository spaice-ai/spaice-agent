def cmd_install(args: argparse.Namespace) -> int:
    agent_id = args.agent_id
    hook_dir = _hook_dir(agent_id)

    if hook_dir.exists() and not args.force:
        backup = hook_dir.with_name(
            hook_dir.name + f".backup-{int(datetime.now().timestamp())}"
        )
        print(f"Backing up existing hook → {backup}")
        shutil.move(str(hook_dir), str(backup))

    hook_dir.mkdir(parents=True, exist_ok=True)

    # handler.py shim
    shim = HOOK_SHIM_TEMPLATE.format(
        generated_at=datetime.now(timezone.utc).isoformat(),
        version=_get_version(),
        agent_id=agent_id,
    )
    (hook_dir / "handler.py").write_text(shim)

    # HOOK.yaml
    (hook_dir / "HOOK.yaml").write_text(
        HOOK_YAML_TEMPLATE.format(agent_id=agent_id)
    )

    # Install manifest
    manifest = {
        "agent_id": agent_id,
        "package_version": _get_version(),
        "installed_at": datetime.now(timezone.utc).isoformat(),
        "shim_template_version": "1.0",
    }
    (hook_dir / ".spaice-install.json").write_text(
        json.dumps(manifest, indent=2)
    )

    print(f"✓ Installed spaice-{agent_id} hook at {hook_dir}")
    print(f"  Package version: {_get_version()}")

    # Optional: scaffold config.yaml
    if getattr(args, "with_config", False):
        cfg_path = _config_path(agent_id)
        if cfg_path.exists() and not args.force:
            print(f"  Config already exists at {cfg_path} — skipped (use --force to overwrite)")
        else:
            cfg_path.parent.mkdir(parents=True, exist_ok=True)
            template = _load_config_template()
            rendered = template.format(
                agent_id=agent_id,
                generated_at=datetime.now(timezone.utc).isoformat(),
                version=_get_version(),
            )
            cfg_path.write_text(rendered)
            print(f"✓ Config scaffold written to {cfg_path}")
            print()
            print("  NEXT STEPS:")
            print(f"  1. Edit {cfg_path} — fill in TODO values")
            print(f"  2. Add credentials to ~/.Hermes/credentials/")
            print(f"     (openrouter.key, exa.json, brave.json at minimum)")
            print(f"  3. Run: spaice-agent doctor {agent_id}")

    return 0


