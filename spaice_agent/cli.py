def _hook_dir(agent_id: str) -> Path:
    return Path.home() / ".Hermes" / "hooks" / f"spaice-{agent_id}"


def _config_dir(agent_id: str) -> Path:
    return Path.home() / ".spaice-agents" / agent_id


def _config_path(agent_id: str) -> Path:
    return _config_dir(agent_id) / "config.yaml"


def _get_version() -> str:
    try:
        return metadata.version("spaice-agent")
    except metadata.PackageNotFoundError:
        return "unknown"


def _load_config_template() -> str:
    """Load the packaged config.yaml.template.

    Uses importlib.resources so it works from wheel installs, editable
    installs, and source checkouts.
    """
    try:
        return (
            resources.files("spaice_agent.templates")
            .joinpath("config.yaml.template")
            .read_text()
        )
    except (AttributeError, FileNotFoundError):
        # Fallback for older Python or non-standard installs
        pkg_root = Path(__file__).parent
        return (pkg_root / "templates" / "config.yaml.template").read_text()


