# ---------------------------------------------------------------------------
# doctor
# ---------------------------------------------------------------------------

def cmd_doctor(args: argparse.Namespace) -> int:
    agent_id = args.agent_id
    hook_dir = _hook_dir(agent_id)
    ok = True

    def check(label: str, passed: bool, detail: str = "") -> None:
        nonlocal ok
        mark = "✓" if passed else "✗"
        print(f"  {mark} {label}" + (f"  ({detail})" if detail else ""))
        if not passed:
            ok = False

    print(f"Doctor: spaice-{agent_id}")
    check("Hook dir exists", hook_dir.exists(), str(hook_dir))
    check("handler.py exists", (hook_dir / "handler.py").exists())
    check("HOOK.yaml exists", (hook_dir / "HOOK.yaml").exists())

    manifest_path = hook_dir / ".spaice-install.json"
    check("Install manifest exists", manifest_path.exists())

    if manifest_path.exists():
        try:
            manifest = json.loads(manifest_path.read_text())
            installed_ver = manifest.get("package_version", "?")
            current_ver = _get_version()
            check(
                "Shim version matches package",
                installed_ver == current_ver,
                f"shim={installed_ver} pkg={current_ver}",
            )
        except Exception as exc:
            check("Manifest parses", False, str(exc))

    try:
        import spaice_agent  # noqa: F401
        check("spaice_agent importable", True)
    except ImportError as exc:
        check("spaice_agent importable", False, str(exc))

    # Config file exists
    cfg_path = _config_path(agent_id)
    check("Config file exists", cfg_path.exists(), str(cfg_path))

    # Config loadable (deeper check — YAML parse + schema validate)
    if cfg_path.exists():
        try:
            from spaice_agent.config import load_agent_config
            load_agent_config(agent_id)
            check("Agent config loads + validates", True)
        except Exception as exc:
            check("Agent config loads + validates", False, str(exc))

    # Antigravity skill library (informational — not required for hook to work)
    antigravity_dir = Path.home() / ".hermes" / "skills" / "antigravity"
    if antigravity_dir.exists():
        skill_count = len(list(antigravity_dir.glob("*/SKILL.md")))
        check(
            "Antigravity skill library installed",
            skill_count > 0,
            f"{skill_count} skills at {antigravity_dir}",
        )
    else:
        # Not a hard failure — optional
        print(f"  ○ Antigravity skill library  (not installed — run `spaice-agent skills install`)")

    return 0 if ok else 1


# ---------------------------------------------------------------------------
# skills — manage the antigravity-awesome-skills library
# ---------------------------------------------------------------------------

def cmd_skills(args: argparse.Namespace) -> int:
    action = args.skills_action
    antigravity_dir = Path.home() / ".hermes" / "skills" / "antigravity"

    if action == "install":
        if antigravity_dir.exists() and not args.force:
            count = len(list(antigravity_dir.glob("*/SKILL.md")))
            print(f"Already installed at {antigravity_dir} ({count} skills).")
            print("Use --force to reinstall, or `spaice-agent skills update` to refresh.")
            return 0
        if antigravity_dir.exists() and args.force:
            shutil.rmtree(antigravity_dir)
        if not shutil.which("npx"):
            print("✗ npx not found. Install Node.js first:")
            print("    macOS:  brew install node")
            print("    Linux:  apt install nodejs npm  (or see nodejs.org)")
            return 1
        print(f"→ Installing antigravity-awesome-skills to {antigravity_dir}...")
        try:
            subprocess.run(
                ["npx", "--yes", "antigravity-awesome-skills", "--path", str(antigravity_dir)],
                check=True,
            )
        except subprocess.CalledProcessError as exc:
            print(f"✗ Install failed: {exc}")
            return 1
        count = len(list(antigravity_dir.glob("*/SKILL.md")))
        print(f"✓ Installed {count} skills at {antigravity_dir}")
        return 0

    elif action == "update":
        if not antigravity_dir.exists():
            print(f"Not installed. Run `spaice-agent skills install` first.")
            return 1
        print(f"→ Removing old install...")
        shutil.rmtree(antigravity_dir)
        print(f"→ Reinstalling latest from antigravity-awesome-skills...")
        try:
            subprocess.run(
                ["npx", "--yes", "antigravity-awesome-skills", "--path", str(antigravity_dir)],
                check=True,
            )
        except subprocess.CalledProcessError as exc:
            print(f"✗ Update failed: {exc}")
            return 1
        count = len(list(antigravity_dir.glob("*/SKILL.md")))
        print(f"✓ Updated to latest ({count} skills)")
        return 0

    elif action == "remove":
        if not antigravity_dir.exists():
            print("Not installed — nothing to remove.")
            return 0
        shutil.rmtree(antigravity_dir)
        print(f"✓ Removed {antigravity_dir}")
        return 0

    elif action == "status":
        if not antigravity_dir.exists():
            print("Antigravity skill library: NOT INSTALLED")
            print("  Install with: spaice-agent skills install")
            return 1
        count = len(list(antigravity_dir.glob("*/SKILL.md")))
        size_mb = sum(f.stat().st_size for f in antigravity_dir.rglob("*") if f.is_file()) // (1024 * 1024)
        print(f"Antigravity skill library: INSTALLED")
        print(f"  Path:   {antigravity_dir}")
        print(f"  Skills: {count}")
        print(f"  Size:   {size_mb} MB")
        return 0

    return 1


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------

def main(argv: Optional[List[str]] = None) -> int:
    parser = argparse.ArgumentParser(
        prog="spaice-agent",
        description="SPAICE Agent Framework management CLI",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    p_install = sub.add_parser("install", help="Install hook shim for an agent")
    p_install.add_argument("agent_id", help="Agent ID (e.g. jarvis, scope-bot)")
    p_install.add_argument(
        "--force", action="store_true",
        help="Overwrite existing hook without backup",
    )
    p_install.add_argument(
        "--with-config", action="store_true",
        help="Also scaffold ~/.spaice-agents/<id>/config.yaml from template",
    )
    p_install.set_defaults(func=cmd_install)

    p_uninstall = sub.add_parser(
        "uninstall", help="Remove hook (optionally also config + state)",
    )
    p_uninstall.add_argument("agent_id", help="Agent ID to remove")
    p_uninstall.add_argument(
        "--purge", action="store_true",
        help="Also delete config + state (~/.spaice-agents/<id>/)",
    )
    p_uninstall.add_argument(
        "--keep-backup", action="store_true",
        help="Rename hook dir to backup instead of deleting",
    )
    p_uninstall.set_defaults(func=cmd_uninstall)

    p_list = sub.add_parser("list", help="Show all installed agents")
    p_list.set_defaults(func=cmd_list)

    p_upgrade = sub.add_parser(
        "upgrade",
        help="Upgrade package + refresh all installed hook shims",
    )
    p_upgrade.add_argument(
        "--skip-pip", action="store_true",
        help="Skip pip reinstall (just refresh shims)",
    )
    p_upgrade.set_defaults(func=cmd_upgrade)

    p_version = sub.add_parser("version", help="Print installed package version")
    p_version.set_defaults(func=cmd_version)

    p_doctor = sub.add_parser(
        "doctor", help="Check install health for an agent",
    )
    p_doctor.add_argument("agent_id", help="Agent ID to check")
    p_doctor.set_defaults(func=cmd_doctor)

    # skills — manage the antigravity-awesome-skills library
    p_skills = sub.add_parser(
        "skills",
        help="Manage the antigravity-awesome-skills library (1,443+ skills)",
    )
    p_skills.add_argument(
        "skills_action",
        choices=["install", "update", "remove", "status"],
        help="Action: install (first time), update (refresh to latest), remove, or status",
    )
    p_skills.add_argument(
        "--force", action="store_true",
        help="For 'install': overwrite existing library",
    )
    p_skills.set_defaults(func=cmd_skills)

    args = parser.parse_args(argv)
    return args.func(args)


if __name__ == "__main__":
    sys.exit(main())
