"""Install CLI — ``spaice-agent <command>`` manages agent installs.

Entry point registered in pyproject.toml as ``spaice-agent``.

Commands:
    install <agent_id>              Write hook shim to ~/.Hermes/hooks/
        --with-config               Also scaffold ~/.spaice-agents/<id>/config.yaml
        --force                     Overwrite existing hook without backup
    uninstall <agent_id>            Remove hook; keep config (with --purge: remove both)
    upgrade                         Reinstall package + refresh all shims
    list                            Show all installed agents
    doctor <agent_id>               Check install health (9 checks)
    version                         Print installed package version

The hook shim is a 13-line autogenerated file that imports from
spaice_agent.hook. All business logic stays in the package, so bugs
fix once and ship via ``spaice-agent upgrade``.
"""
from __future__ import annotations

import argparse
import json
import os
import shutil
import subprocess
import sys
from datetime import datetime, timezone
from importlib import metadata, resources
from pathlib import Path
from typing import List, Optional

HOOK_SHIM_TEMPLATE = '''"""SPAICE pre-turn middleware shim — autogenerated by spaice-agent install.

Do NOT edit this file by hand. All logic lives in the spaice_agent
package; to upgrade, run ``spaice-agent upgrade``.

Generated: {generated_at}
Package version at install: {version}
Agent ID: {agent_id}
"""
from spaice_agent.hook import make_hook

AGENT_ID = "{agent_id}"
handle, register_tools = make_hook(AGENT_ID)
'''

HOOK_YAML_TEMPLATE = """name: spaice-{agent_id}
description: |
  SPAICE Agent Framework pre-turn middleware. Memory recall, search,
  and FW-1 consensus advisory. Managed by spaice-agent install.
events:
  - agent:start
"""


