def cmd_upgrade(args: argparse.Namespace) -> int:
    if not args.skip_pip:
        print("→ pip install --upgrade --editable <current install>")
        pkg_root = Path(__file__).parent.parent
        try:
            subprocess.run(
                [sys.executable, "-m", "pip", "install", "-e", str(pkg_root)],
                check=True,
            )
        except subprocess.CalledProcessError as exc:
            print(f"✗ pip install failed: {exc}", file=sys.stderr)
            return 1

    agents = _list_installed_agents()
    if not agents:
        print("No spaice-* agents installed; nothing to refresh.")
        return 0

    agent_ids = [a["agent_id"] for a in agents]
    print(f"→ Refreshing {len(agent_ids)} hook shim(s): {', '.join(agent_ids)}")
    for agent_id in agent_ids:
        fake_args = argparse.Namespace(
            agent_id=agent_id, force=True, with_config=False,
        )
        cmd_install(fake_args)

    # Also refresh bundled skills if already installed (stale manifest means user
    # wants them kept in sync with package version)
    bundled_manifest = _hermes_skills_dir() / ".spaice-bundled-manifest.json"
    if bundled_manifest.exists():
        print(f"→ Refreshing bundled skills (package v{_get_version()})")
        cmd_skills(argparse.Namespace(skills_action="bundled-install", force=True))

    # Also refresh vendored antigravity bundle if already installed
    ag_manifest = _antigravity_dir() / ".spaice-antigravity-manifest.json"
    if ag_manifest.exists() or _antigravity_dir().exists():
        print(f"→ Refreshing antigravity vendored bundle (package v{_get_version()})")
        cmd_skills(argparse.Namespace(skills_action="antigravity-install", force=True))

    print(f"✓ Upgrade complete. Package version: {_get_version()}")
    return 0


